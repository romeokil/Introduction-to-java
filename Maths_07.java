import java.util.*;
public class Maths_07 {
    public static void main(String[] args) {
        System.out.println("Starting With Maths.............");
        // min->Maths.min
        // max->Maths.max
        //random->Maths.random

        int a=20;
        int b=30;
        int maxi=Math.max(a,b);
        int mini=Math.min(a,b);
        double randomno=(Math.random());
        System.out.println("Maximum value "+maxi);
        System.out.println("Minimum value "+mini);
        System.out.println("Random value "+randomno);
    }
}
