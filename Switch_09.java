public class Switch_09 {
    public static void main(String[] args) {
        System.out.println("Welcome to Switch case statement");

        int no=7;

        switch(no){
            case 1:
                System.out.println("No is 1");
                break;
            case 2: 
                System.out.println("No is 2");
                break;
            case 3:
                System.out.println("No is 3");
                break;
            case 4:
                System.out.println("No is 4");
                break;
            default:
                System.out.println("Greater than 4");            
            }
            System.out.println("Switch case ended");
    }
}
